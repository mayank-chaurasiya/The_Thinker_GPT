import { MyContext } from "../Context/MyContext";
import "./Chat.css";
import { useContext, useState, useEffect } from "react";
import ReactMarkdown from "react-markdown";
import rehypeHighlight from "rehype-highlight";
import "highlight.js/styles/github-dark.css";

const Chat = ({ chatsContainerRef }) => {
  const { newChat, prevChats, reply } = useContext(MyContext);
  const [latestReply, setLatestReply] = useState(null);

  useEffect(() => {
    if (!prevChats?.length) return;

    const content = reply.split(" "); // get individual words
    let idx = 0;
    const interval = setInterval(() => {
      setLatestReply(content.slice(0, idx + 1).join(" "));
      idx++;
      // Scroll to bottom as new text is generated
      if (chatsContainerRef?.current) {
        chatsContainerRef.current.scrollTop =
          chatsContainerRef.current.scrollHeight;
      }
      if (idx >= content.length) clearInterval(interval);
    }, 40);
    return () => clearInterval(interval);
  }, [prevChats, reply, chatsContainerRef]);

  return (
    <>
      {newChat && <h1>Where should we begin ?</h1>}
      <div className="chats" ref={chatsContainerRef}>
        {prevChats?.slice(0, -1).map((chat, idx) => (
          <div
            className={chat.role === "user" ? "userDiv" : "gptDiv"}
            key={idx}
          >
            {chat.role === "user" ? (
              <p className="userMessage">{chat.content}</p>
            ) : (
              <ReactMarkdown rehypeHighlight={rehypeHighlight}>
                {chat.content}
              </ReactMarkdown>
            )}
          </div>
        ))}
        {prevChats.length > 0 && latestReply != null && (
          <div className="gptDiv" key={"typing"}>
            <ReactMarkdown rehypeHighlight={rehypeHighlight}>
              {latestReply}
            </ReactMarkdown>
          </div>
        )}
      </div>
    </>
  );
};

export default Chat;
